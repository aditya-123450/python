# A simple function to calculate the area of a rectangle
def calculate_area(length, width):
  area = length * width
  return area

# Calling the function with different arguments
area1 = calculate_area(10, 5)  # Result is 50
area2 = calculate_area(7, 3)   # Result is 21

print(f"Area 1: {area1}, Area 2: {area2}")